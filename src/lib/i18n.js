export const LANG_STORAGE_KEY = 'home-designer-pro-lang';
export const DEFAULT_LANG = 'id';

export function getStoredLang() {
  if (typeof localStorage === 'undefined') return DEFAULT_LANG;
  return localStorage.getItem(LANG_STORAGE_KEY) || DEFAULT_LANG;
}

export function setStoredLang(lang) {
  if (typeof localStorage !== 'undefined') localStorage.setItem(LANG_STORAGE_KEY, lang);
}

// React hook for translations
import React from 'react';
const LangContext = React.createContext({
  lang: DEFAULT_LANG,
  t: translations[DEFAULT_LANG],
  setLang: () => {},
});

export function LangProvider({ children }) {
  const [lang, setLangState] = React.useState(getStoredLang());

  const setLang = React.useCallback((newLang) => {
    setLangState(newLang);
    setStoredLang(newLang);
  }, []);

  const value = React.useMemo(() => ({
    lang,
    t: translations[lang] || translations[DEFAULT_LANG],
    setLang,
  }), [lang, setLang]);

  return React.createElement(LangContext.Provider, { value }, children);
}

export function useLang() {
  return React.useContext(LangContext);
}
